CREATE TABLE MEMBER
(
ID  VARCHAR2(30),
AUTH NUMBER(1),
PASSWORD VARCHAR2(100),
NAME VARCHAR2(50),
MEMBER_NO NUMBER(50),
JOIN_DATE VARCHAR2(8) DEFAULT SYSDATE --가입일 20160101
)
;

CREATE TABLE MEMBER_INFO
(
MEMBER_NO NUMBER(50),
PHONE1 VARHCAR2(5), -- 010
PHONE2 NUMBER(8), -- 1234-5678
ADDRESS1 VARCHAR2(1000), -- 기본주소
ADDRESS2 VARCHAR2(1000), -- 상세주소
POST_NO NUMBER(5) DEFAULT 00000 --우편번호
)
;

